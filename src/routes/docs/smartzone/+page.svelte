<h2>Smartzone</h2>

<h3>GET</h3>

<p>
	Selecteer één <em>smartzone</em> op basis van een meegestuurde url parameter met het
	id.
</p>

<h4>Url parameters:</h4>
<ul>
	<li>
		<strong>id</strong>, het id van de te selecteren <em>smart zone</em>.
		<br />(Vind jouw id door de lijst met smartzones te gebruiken!)
	</li>
</ul>

<h4>Voorbeeld Request:</h4>

<ul>
	<li>
		<a href="/api/v1/smartzone?id=clene4gw60aqg0bunwwpawr1p"
			>/api/v1/smartzone?id=clene4gw60aqg0bunwwpawr1p</a
		>
	</li>
</ul>

<h3>[slug]</h3>

<p>
	Selecteer één <em>smartzone</em> op basis van de ingestelde [slug] welke als onderdeel
	van de url wordt meegestuurd.
</p>

<h4>Voorbeeld Request:</h4>

<ul>
	<li>
		    <a href="/api/v1/smartzone/tijn">/api/v1/smartzones</a>
	</li>
</ul>


<h4>Voorbeeld requests:</h4>
<ul>
  <li>
    <a href="/api/v1/smartzones/">/api/v1/smartzones</a>
  </li>
</ul>

